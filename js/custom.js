let navLogo = document.getElementById('navLogo');

function navLogoHover() {
     navLogo.style.fontSize = '25px';
     navLogo.style.color = '#ffb320';
     navLogo.style.transition = 'ease 0.25s';
}

function navLogoHoverOver() {
     navLogo.style.fontSize = '24px';
     navLogo.style.color = 'white';
     navLogo.style.transition = 'ease 0.25s';
}
